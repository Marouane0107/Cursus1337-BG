int	ft_atoi(const char	*str)
{
	int	x;
	int	w;
	int	res;

	res = 0;
	x = 0;
	w = 1;
	while (str[x] <= 32)
		x++;
	if (str[x] == '-')
	{
		w = -1;
		x++;
	}
	else if (str[x] == '+')
	{
			w = 1;
			x++;
	}
	while (str[x] >= '0' && str[x] <= '9')
	{
		res = res * 10 + str[x] - 48;
		x++;
	}
	return (res * w);
}
