#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	x;

	x = 0;
	if (s && f)
	{
		while (s[x] != '\0')
		{
			f(x, &s[x]);
			x++;
		}
	}
}
