#include <stdlib.h>
int	ft_strncmp(const char	*s1, const char	*s2, size_t	n)
{
	size_t	x;

	x = 0;
	while (x < n)
	{
		if (s1[x] != s2[x])
			return (s1[x] - s2[x]);
		x++;
	}
	return (s1[x] - s2[x]);
}
